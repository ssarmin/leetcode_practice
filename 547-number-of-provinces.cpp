//https://leetcode.com/problems/number-of-provinces
class Solution {
public:
    vector<int> parent;
    int find(int i){
        if(parent[i] == i)
            return i;
        return find(parent[i]);
    }
    void init(int n){
        parent.resize(n);
        for(int i=0; i<n; i++){
            parent[i] = i;
        }
    }
    int findCircleNum(vector<vector<int>>& isConnected) {
        init(isConnected.size());
        int res = isConnected.size();
        for (int r = 0; r < isConnected.size(); r++) {
            for (int c = r+1; c < isConnected.size(); c++) {
                int p1 = find(r);
                int p2 = find(c);
                if(isConnected[r][c] == 1 && p1 != p2){
                    res--;
                    parent[p1] = p2;
                }
            }
        }
        return res;
    }
};

