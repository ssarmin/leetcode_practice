//https://leetcode.com/problems/max-consecutive-ones-ii
class Solution {
public:
    int findMaxConsecutiveOnes(vector<int>& nums) {
        bool one = true;
        int left = 0;
        int res = 0;
        for(int right = 0; right < nums.size(); right++){
            if(nums[right] == 0){
                if(one){
                    one = false;
                }else{
                    while(nums[left] != 0){
                        left++;
                    }
                    left++;
                }
            }
            res = max(res, right - left + 1);
        }
        return res;
    }
};

