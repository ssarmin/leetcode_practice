//https://leetcode.com/problems/max-points-on-a-line
class Solution {
public:
    int maxPoints(vector<vector<int>>& points) {
        int max_val = 0;
        for(int i=0; i<points.size(); i++){
            unordered_map<double, int> m;
            for(int j=0; j<points.size(); j++){
                if(i == j)continue;
                int dx = points[i][0] - points[j][0];
                if(dx == 0){
                    m[(double)INT_MAX]++;
                    max_val = max(max_val, m[(double)INT_MAX]);
                }else{
                    double slope = (double)(points[i][1] - points[j][1])/(double)dx;
                    m[slope]++;
                    max_val = max(max_val, m[slope]);
                }
            }
        }

        return max_val+1;
    }
};
